// And operator
let x = 1;
let y = 2;
let z = 3;

console.log( x < y && y < z); // true
console.log(x > y && y < z); // false

// OR operator

console.log( x > y || y < z); // true
console.log( x > y || y > z); // false

// Not
let a = true;
console.log(!a);

let b = 2;
let c = 3;
console.log(!(b < c));
