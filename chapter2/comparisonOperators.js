// the outcome of the comparison operators is always a Boolean value

// Equal
let x = 5;
let y = "5";
console.log(x == y);
console.log(x === y);

// Not equal

let n = 5;
let n2 = "5";
console.log(n != n2);
console.log(n !== n2);

